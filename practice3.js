// Addition

const num1 = 70;
const num2 = 20;

const sum = num1 + num2;
console.log('The Sum is:', sum);

// Subtraction

const myNum1 = 80;
const myNum2 = 30;

const subtraction = myNum1 - myNum2;
console.log('Subtraction Result is:', subtraction);

// Multiplication

const myNumber1 = 20;
const myNumber2 = 5;

const multiply = myNumber1 * myNumber2;
console.log('Multiplied numbers are:', multiply);

// Division

const Number1 = 100;
const Number2 = 5;

const divide = Number1 / Number2;
console.log('Quotient numbers are:', divide);

// modular forms

const firstNumber = 100;
const secondNumber = 6;

const result = firstNumber % secondNumber;
console.log('The Result is:', result);